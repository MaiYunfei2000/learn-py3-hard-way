# 2.3.6 数组

import numpy as np

# 创建3*2(3行2列)数组，所有单元值都为0，赋给变量a
a = np.zeros([3,2])
print(a)

print()

# 时刻注意，Python以0为起始值，即第0行/列实际上为第1行/列。

# (行，列)
# 将数组a的(1,1)单元赋值为1
a[0,0] = 1
# 将数组a的(1,2)单元赋值为2
a[0,1] = 2
# 将数组a的(2,1)单元赋值为9
a[1,0] = 9
# 将数组a的(3,2)单元赋值为12
a[2,1] = 12
print(a)

print()

# 以下展示打印数组个别单元的值的方法

# 打印数组a的(1,2)单元的值，即为2
print(a[0,1])
# 将数组a的(2,1)单元的值即9赋给变量v
v = a[1,0]
print(v)

